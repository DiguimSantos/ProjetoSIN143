    private void jNomeUsuarioComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jNomeUsuarioComboBoxActionPerformed
        // TODO add your handling code here:
        try {
            String gerente; 
            if(/*MoradorController.buscaMoradores()*/mor.get(jNomeUsuarioComboBox.getSelectedIndex()).getGerente()==1) gerente = "Sim";
            else{gerente = "Não";}
            
            this.jCPF.setText("CPF: "+/*MoradorController.buscaMoradores()*/mor.get(jNomeUsuarioComboBox.getSelectedIndex()).getCpf());
            this.jNome.setText("Login: "+/*MoradorController.buscaMoradores()*/mor.get(jNomeUsuarioComboBox.getSelectedIndex()).getLogin());
            this.jGerente.setText("Gerente: "+gerente);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro "+ex);
        }
    }//GEN-LAST:event_jNomeUsuarioComboBoxActionPerformed

    private void jSelecaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSelecaoActionPerformed
        // TODO add your handling code here:
        try{
            if(MoradorController.removeMorador(this.jNomeUsuarioComboBox.getSelectedItem().toString(), MoradorController.buscaMoradores().get(jNomeUsuarioComboBox.getSelectedIndex()).getCpf())){
                JOptionPane.showMessageDialog(this, "Morador removido com sucesso");
                dispose();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ocorreu um erro "+e);
        }
    }//GEN-LAST:event_jSelecaoActionPerformed

